	<p><h4>
	Hangman is a classic paper and pencil game. The idea of the gallows may be a little morbid, but it also adds to the excitement of the game, especially for boys. Everyone wants to save the poor person from a grim fate!
    </h4></p>
    <h4>
	NUMBER OF PLAYERS: 2 (or more).
	</h4>
	 1 must be the executioner, and 1 or more letter guessers.
     <h5>
	WHAT'S NEEDED TO PLAY: 
	 <h5>
	A pencil and some paper are needed to play the game. A clipboard is also a handy hard surface to play on.

	SETUP: Either print out some pre-prepared hangman pages or draw a simple gallows and write out the alphabet above or below the gallows. To save time you can print out one of these hangman pages:

		Hangman 1 Page : Includes to games per page with gallows, alphabet, and dotted figure of a person for the executioner to outline as the game progresses.
		Hangman 2 Page : Same as hangman 1 except without the dotted figure of a stick person person.
		Hangman 3 Page : Same as hangman 2 but with 4 games instead of two.

	HOW TO PLAY: Choose one person to be the executioner. That person will think of a word or short phrase an mark out blanks (short lines) for each letter of each word. Separate words with either a slash, a fairly wide gap, or place words on separate lines. Then another player will guess a letter. If that letter is in the word(s) then write the letter in everywhere it would appear, an cross out that letter in the alphabet. If the letter isn't in the word then add a body part to the gallows (head, body, left arm, right arm, left leg, right leg). The player will continue guessing letters until they can either solve the word (or phrase) or all six body parts are on the gallows.

	TO WIN: The executioner wins if the full body is hanging from the gallows. The guesser(s) wins if they guess the word before the person is hung.
